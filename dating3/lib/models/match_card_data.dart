class MatchCardData {
  final String name;
  final int age;
  final String distance;
  final String matchPercentage;
  final String imageAsset;
  final String location;
  final int matchPercentValue;
  final String about;

  MatchCardData({
    required this.name,
    required this.age,
    required this.distance,
    required this.matchPercentage,
    required this.imageAsset,
    required this.location,
    required this.matchPercentValue,
    required this.about,
  });
}
